package org.chengcai.day01;//  声明该类在day01包下
/**
 * public   公开，公共，代表这个类的访问权限
 * class   代表类
 *
 */
public class HelloWorld01 { //声明这是一个访问权限公开的类	
	//该方法，方法名字为main(主要)，程序运行先找到main方法，然后运行	
	//static 静态  ，void 无返回值  
	public static void main(String[] args) {				
		System.out.println("hello,world");
		//打印括号里面的内容   system 系统    out输出   print打印
		System.out.println(1+1);
		System.out.println(5*2);
	    System.out.print(1);
	    
	}

}
